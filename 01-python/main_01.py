'''
    Запросить имя и возраст
    Если возраст менее 18 лет, то выдать сообщение - "Это сервис 18+, до свидания!", 
    Иначе запросить номер знака зодиака [0..11]
    ["Козерог","Водолей","Рыбы","Овен","Телец","Близнецы","Рак","Лев","Дева","Весы","Скорпион","Стрелец"]

    Запросить номер знака зодиака близкого человека
    Запросить номер стратегии - 0-Эффект wow  1-Научная

    Выдать совместимость знаков по номеру стратегии:
    0 - если длина знаков зодиака отличается не более чем на 2 знака, то ВЫ СОВМЕСТИМЫ
    1 - если знак зодиака совпадает или соседний, то ВЫ СОВМЕСТИМЫ

    иначе сообщение "найдите партнера тут <ссылка на google.com>"
'''

def main():
    # Создаем список знаков зодиака
    zodiac_signs = ["Козерог", "Водолей", "Рыбы", "Овен", "Телец", "Близнецы", "Рак", 
                    "Лев", "Дева", "Весы", "Скорпион", "Стрелец"]

    # Запрашиваем имя пользователя и сохраняем его в переменную name
    name = input("Введите ваше имя: ")

    # Запрашиваем возраст пользователя и преобразуем введенное значение в целое число
    age = int(input("Введите ваш возраст: "))

    # Проверяем, если пользователь младше 18 лет, выводим сообщение и завершаем программу
    if age < 18:
        print("Это сервис 18+, до свидания!")
        return

    # Запрашиваем номер знака зодиака пользователя и преобразуем его в целое число
    user_zodiac_index = int(input(f"Введите номер вашего знака зодиака (0-11): "))

    # Проверяем корректность введенного номера знака зодиака
    if user_zodiac_index < 0 or user_zodiac_index > 11:
        print("Неверный номер зодиака.")
        return

    # Запрашиваем номер знака зодиака близкого человека и преобразуем его в целое число
    partner_zodiac_index = int(input(f"Введите номер знака зодиака близкого человека (0-11): "))

    # Проверяем корректность введенного номера знака зодиака близкого человека
    if partner_zodiac_index < 0 or partner_zodiac_index > 11:
        print("Неверный номер зодиака.")
        return

    # Запрашиваем номер стратегии и преобразуем его в целое число
    strategy = int(input("Введите номер стратегии (0-Эффект wow, 1-Научная): "))

    # Проверяем корректность введенного номера стратегии
    if strategy != 0 and strategy != 1:
        print("Неверный номер стратегии.")
        return

    # Определяем совместимость знаков в зависимости от выбранной стратегии
    if strategy == 0:
        # Стратегия 0: Эффект wow - проверяем, если разница номеров знаков зодиака не более 2
        compatibility = abs(user_zodiac_index - partner_zodiac_index)
        if compatibility <= 2:
            print("ВЫ СОВМЕСТИМЫ")
        else:
            print("Найдите партнера тут <https://google.com>")
    elif strategy == 1:
        # Стратегия 1: Научная - проверяем, совпадают ли номера знаков зодиака или они соседние
        if user_zodiac_index == partner_zodiac_index or abs(user_zodiac_index - partner_zodiac_index) == 1:
            print("ВЫ СОВМЕСТИМЫ")
        else:
            print("Найдите партнера тут <https://google.com>")

# Запускаем основную функцию main, если скрипт выполняется напрямую
if __name__ == "__main__":
    main()